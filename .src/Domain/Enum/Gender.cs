namespace Domain.Enum;

/// <summary>
/// Represents the gender of a user.
/// </summary>
public enum Gender
{
    /// <summary>
    /// Represents a female user.
    /// </summary>
    Female = 0,

    /// <summary>
    /// Represents a male user.
    /// </summary>
    Male = 1,

    /// <summary>
    /// Represents a user whose gender is unknown.
    /// </summary>
    Unknown = 2
}